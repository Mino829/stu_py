# 01_print_and_variables/lesson.py
# レッスン1: 画面に文字を出してみよう & 変数

# 1. 画面への出力 (print)
# 括弧の中に入れた文字や数字が表示されます。
print("こんにちは！Pythonの世界へようこそ。")
print(123)

# 2. 変数 (Variable)
# 変数は「データを入れる箱」のようなものです。
# 名前を付けて、あとで使い回すことができます。
message = "はじめての変数"
print(message)

# 3. 変数の上書き
# 箱の中身を入れ替えることができます。
score = 100
print(score)

score = 150 # 上書き
print(score)

