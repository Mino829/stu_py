# レッスン3: 計算してみよう (演算子)

# 1. 四則演算
a = 10
b = 3

print("足し算:", a + b)
print("引き算:", a - b)
print("掛け算:", a * b)
print("割り算:", a / b)

# 2. ちょっと特殊な計算
print("割り算の答え（整数）:", a // b) # 3
print("割り算の余り:", a % b)       # 1
print("べき乗（10の3乗）:", a ** b)   # 1000

# 3. 計算の順序
# 数学と同じで、掛け算・割り算が先です。
# 括弧 () を使うと優先順位を変えられます。
result = (10 + 5) * 2
print("計算順序のテスト:", result) # 30
